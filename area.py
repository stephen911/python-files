print("Calculating the area of a triangle")
print("The formular for finding area of a triangle is 1/2*base*height ")
height = input("What is the height of the triangle? ")
print("The height of the triangle is:",height)
base = input("What is the length of the base in centimeters?")
print("The base is :",base)
Height = int(height)
Base = int(base)
Area = 0.5 * Height * Base
print("The area of the triangle is "+ str(Area) + "cm")

